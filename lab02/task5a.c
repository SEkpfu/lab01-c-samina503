#include <stdio.h>

int main() 
{
    double r, a, b, z, c;

    // Ввод данных
    printf("Введите радиус торта r: ");
    scanf("%lf", &r);
    printf("Введите размеры коробки a и b: ");
    scanf("%lf %lf", &a, &b);
    printf("Введите высоту торта z и высоту коробки c: ");
    scanf("%lf %lf", &z, &c);

    // Проверка для горизонтального положения
    int goriz = (2 * r <= a && 2 * r <= b);

    // Проверка для вертикального положения
    int vertic = (2 * r <= a && 2 * r <= b && z <= c);

    if (goriz) {
        printf("Торт поместится горизонтально.\n");
    } else {
        printf("Торт не поместится горизонтально.\n");
    }

    if (vertic) {
        printf("Торт поместится вертикально.\n");
    } else {
        printf("Торт не поместится вертикально.\n");
    }

    return 0;
}